function formatMessage(template, params = {}) {
  return template.replace(/\{(\w+)\}/g, (_, key) => String(params[key] ?? ""));
}

function getMessages(languageCode) {
  return UI_MESSAGES[languageCode] ?? UI_MESSAGES.es;
}

function stripSimulatedPrefix(text) {
  for (const prefix of simulatedPrefixes) {
    if (text === prefix) {
      return "";
    }

    if (text.startsWith(`${prefix} `)) {
      return text.slice(prefix.length + 1);
    }
  }

  return text;
}

export function getUiText(key, languageCode = "es", params = {}) {
  const messages = getMessages(languageCode);
  const fallbackMessages = UI_MESSAGES.es;
  const template = messages[key] ?? fallbackMessages[key] ?? key;
  return formatMessage(template, params);
}

export function getLanguageDisplayLabel(languageCode, uiLanguageCode = "es") {
  const languageOption = getLanguageOption(languageCode);
  if (!languageOption) {
    return languageCode;
  }

  return getUiText(languageOption.translationKey, uiLanguageCode);
}

export function translateDynamicText(text, languageOption) {
  if (!languageOption) {
    return text;
  }

  const cleanText = stripSimulatedPrefix(text);
  return cleanText ? `${languageOption.simulatedPrefix} ${cleanText}` : languageOption.simulatedPrefix;
}
